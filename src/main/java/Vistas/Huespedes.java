    private void llenarTablaHuespedes() {
   
    DefaultTableModel modelo = new DefaultTableModel();
    
    
    modelo.addColumn("ID Huesped");
    modelo.addColumn("Nombre");
    modelo.addColumn("Dirección");
    modelo.addColumn("Teléfono");
    
    
    HuespedesDAO dao = new HuespedesDAO();
    List<HuespedesDTO> listaHuespedes = dao.obtenerHuespedes();
    
    
    for (HuespedesDTO huesped : listaHuespedes) {
       
        Object[] fila = new Object[4];
        fila[0] = huesped.getIdHuesped();
        fila[1] = huesped.getNombre();
        fila[2] = huesped.getDireccion();
        fila[3] = huesped.getTelefono();
        
        
        modelo.addRow(fila);
    }
    
    
    jTbHuespedes.setModel(modelo);
}


