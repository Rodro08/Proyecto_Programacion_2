    private void jBtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAgregarActionPerformed
        
    String nombre = jTxtNombre.getText().trim();
    String direccion = jTxtDireccion.getText().trim();
    String telefono = jTxtTelefono.getText().trim();
    
    
    if (nombre.isEmpty() || direccion.isEmpty() || telefono.isEmpty()) {
        
        JOptionPane.showMessageDialog(this, "Todos los campos deben estar llenos.", "Error", JOptionPane.ERROR_MESSAGE);
    } else {
        
        HuespedesDTO nuevoHuesped = new HuespedesDTO(0, nombre, direccion, telefono);  
        
       
        HuespedesDAO huespedesDAO = new HuespedesDAO();
        
        
        boolean exito = huespedesDAO.agregarHuesped(nuevoHuesped);
        
        if (exito) {
            
            JOptionPane.showMessageDialog(this, "Huésped agregado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            llenarTablaHuespedes();
        } else {
            
            JOptionPane.showMessageDialog(this, "Hubo un error al agregar al huésped.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    }//GEN-LAST:event_jBtnAgregarActionPerformed

    private void jBtnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEditarActionPerformed
          
    int selectedRow = jTbHuespedes.getSelectedRow();
    
    if (selectedRow != -1) { 
        
        int idHuesped = (int) jTbHuespedes.getValueAt(selectedRow, 0);
        
        
         String nombre = jTxtNombre.getText().trim();
        String direccion = jTxtDireccion.getText().trim();
        String telefono = jTxtTelefono.getText().trim();
        
        
        if (nombre.isEmpty() || direccion.isEmpty() || telefono.isEmpty()) {
            
            JOptionPane.showMessageDialog(this, "Todos los campos deben estar llenos.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            
            HuespedesDTO huesped = new HuespedesDTO(idHuesped, nombre, direccion, telefono);
            
            
            HuespedesDAO huespedesDAO = new HuespedesDAO();
            
          
            boolean exito = huespedesDAO.actualizarHuesped(huesped);
            
            if (exito) {
               
                JOptionPane.showMessageDialog(this, "Huésped actualizado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
                llenarTablaHuespedes(); 
            } else {
               
                JOptionPane.showMessageDialog(this, "Hubo un error al actualizar el huésped.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    } else {
        
        JOptionPane.showMessageDialog(this, "Por favor, seleccione un huésped de la tabla.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jBtnEditarActionPerformed

    private void jBtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnEliminarActionPerformed
       
    int selectedRow = jTbHuespedes.getSelectedRow();
    
    if (selectedRow != -1) { // Verificar si se ha seleccionado una fila
       
        int idHuesped = (int) jTbHuespedes.getValueAt(selectedRow, 0);
        
       
        HuespedesDAO huespedesDAO = new HuespedesDAO();
        
       
        boolean exito = huespedesDAO.eliminarHuesped(idHuesped);
        
        if (exito) {
            
            JOptionPane.showMessageDialog(this, "Huésped eliminado exitosamente.", "Éxito", JOptionPane.INFORMATION_MESSAGE);
            llenarTablaHuespedes(); 
        } else {
           
            JOptionPane.showMessageDialog(this, "Hubo un error al eliminar el huésped.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } else {
        
        JOptionPane.showMessageDialog(this, "Por favor, seleccione un huésped de la tabla.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jBtnEliminarActionPerformed

